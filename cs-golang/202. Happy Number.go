func isHappy(n int) bool {
    m := make(map[int]bool)
	res := 0
	for _,ok := m[n];ok == false;{
		m[n] = true
		for n != 0{
			t := n%10
			res += t*t
			n = n/10
		}
		if res == 1{
			return true
		}
		n = res
		res = 0
		_,ok = m[n]
	}
	return false
}
